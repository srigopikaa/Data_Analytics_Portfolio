-- Insert consolidated data by joining multiple normalized tables:
-- customers, orders, order_items, products, payments

-- This transforms transactional data into an analytics-ready dataset

 INSERT INTO consolidated_dataset 
SELECT
    -- Customer Information
    c.customer_id,

    -- Order Information
    o.order_id,
    o.order_purchase_timestamp AS order_date,

    -- Product Information
    pct.product_category_name_english,
    p.product_id,
    p.product_category_name,

    -- Quantity
    oi.order_item_id,

    -- Revenue = Product Price + Freight
    (oi.price + oi.freight_value),

    -- Payment Method
    pay.payment_type

FROM olist_orders_dataset o
JOIN olist_customers_dataset c
    ON o.customer_id = c.customer_id   -- Foreign key relationship
JOIN olist_order_items_dataset oi
    ON o.order_id = oi.order_id
JOIN olist_products_dataset p
    ON oi.product_id = p.product_id
LEFT JOIN product_category_name_translation pct
    ON p.product_category_name = pct.product_category_name
JOIN olist_order_payments_dataset pay
    ON o.order_id = pay.order_id;
    
