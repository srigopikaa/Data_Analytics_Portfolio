-- Create a professional analytical view for Customer CLV metrics

CREATE VIEW customer_clv AS
SELECT 
    customer_id,

    -- Total Revenue Generated
    SUM(revenue) AS total_revenue,

    -- Total Orders Placed
    COUNT(DISTINCT order_id) AS total_orders,

    -- First Purchase Date
    MIN(order_date) AS first_purchase,

    -- Last Purchase Date
    MAX(order_date) AS last_purchase,

    -- Customer Lifespan in Days
    DATEDIFF(MAX(order_date), MIN(order_date)) AS lifespan_days

FROM consolidated_dataset
GROUP BY customer_id;

-- This query calculates monthly revenue per product
-- and ranks products within each month using RANK()

SELECT *
FROM (
    SELECT
        product_name AS product_id,

        -- Extract Year-Month format
        DATE_FORMAT(order_date,'%y-%m') AS order_date,

        -- Total monthly revenue per product
        SUM(revenue) AS total_revenue,

        -- Rank products within each month
        RANK() OVER(
            PARTITION BY DATE_FORMAT(order_date,'%y-%m')
            ORDER BY SUM(revenue) DESC
        ) AS product_rank

    FROM consolidated_dataset
    GROUP BY product_id, order_date

) AS ranked_products

-- Keep only Top 10 products per month
WHERE product_rank <= 10

-- Sort results
ORDER BY order_date, product_rank;

-- Identify first purchase date and cohort month per customer

SELECT
    customer_id,
    MIN(order_date) AS first_purchase_date,
    DATE_FORMAT(MIN(order_date), '%Y-%m') AS cohort_month
FROM consolidated_dataset
GROUP BY customer_id;

-- Calculate how many months after first purchase
-- each subsequent purchase occurred

SELECT
    cd.customer_id,
    DATE_FORMAT(cd.order_date, '%Y-%m') AS purchase_month,
    DATE_FORMAT(fp.first_purchase_date, '%Y-%m') AS cohort_month,

    -- Month difference calculation
    PERIOD_DIFF(
        DATE_FORMAT(cd.order_date, '%Y%m'),
        DATE_FORMAT(fp.first_purchase_date, '%Y%m')
    ) AS month_number

FROM consolidated_dataset cd
JOIN (
    SELECT
        customer_id,
        MIN(order_date) AS first_purchase_date
    FROM consolidated_dataset
    GROUP BY customer_id
) fp
ON cd.customer_id = fp.customer_id;

-- Count distinct customers per cohort month and month number

INSERT INTO cohort_analysis
SELECT
    DATE_FORMAT(fp.first_purchase_date, '%Y-%m') AS cohort_month,

    PERIOD_DIFF(
        DATE_FORMAT(cd.order_date, '%Y%m'),
        DATE_FORMAT(fp.first_purchase_date, '%Y%m')
    ) AS month_number,

    COUNT(DISTINCT cd.customer_id) AS total_customers

FROM consolidated_dataset cd
JOIN (
    SELECT
        customer_id,
        MIN(order_date) AS first_purchase_date
    FROM consolidated_dataset
    GROUP BY customer_id
) fp
ON cd.customer_id = fp.customer_id

GROUP BY cohort_month, month_number
ORDER BY cohort_month, month_number;

-- Create churn detection table
CREATE TABLE detect_churn_customers (
    customer_id VARCHAR(50),
    last_purchase_date VARCHAR(50),
    months_inactive VARCHAR(50),
    customer_status VARCHAR(50)
);

-- Insert churn status based on inactivity threshold (>= 3 months)

INSERT INTO detect_churn_customers
SELECT
    customer_id,

    -- Most recent purchase
    MAX(order_date) AS last_purchase_date,

    -- Months inactive since latest dataset date
    PERIOD_DIFF(
        DATE_FORMAT((SELECT MAX(order_date) FROM consolidated_dataset), '%Y%m'),
        DATE_FORMAT(MAX(order_date), '%Y%m')
    ) AS months_inactive,

    -- Classification logic
    CASE 
        WHEN PERIOD_DIFF(
                DATE_FORMAT((SELECT MAX(order_date) FROM consolidated_dataset), '%Y%m'),
                DATE_FORMAT(MAX(order_date), '%Y%m')
             ) >= 3 
        THEN 'Churned'
        ELSE 'Active'
    END AS customer_status

FROM consolidated_dataset
GROUP BY customer_id;



